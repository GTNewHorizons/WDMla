package com.gtnewhorizons.wdmla.api.ui.sizer;

/**
 * The width and height of a widget. All elements must be greater than zero.
 * 
 * @see java.awt.Dimension
 */
public interface ISize {

    /**
     * @return width
     */
    float getW();

    /**
     * @return height
     */
    float getH();
}
